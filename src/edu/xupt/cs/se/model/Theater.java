public class Theater {
    private  int id;
    private String name;
    private int studio_number;
    private String addr;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStudio_number() {
        return studio_number;
    }

    public void setStudio_number(int studio_number) {
        this.studio_number = studio_number;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }
}


